#include "cub3d.h"

void	free_split(char **arr)
{
	int	i;

	if (!arr)
		return ;
	i = 0;
	while (arr[i])
	{
		free(arr[i]);
		i++;
	}
	free(arr);
}

void	fatal_cub(t_cub *cub, const char *msg)
{
	write_error(msg);
	destroy_cub(cub);
	exit(1);
}
